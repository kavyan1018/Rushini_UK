# How many types of data are there in Python?

# Answer: There are 4 types of data in Python. They are:

# 1. Numeric Data Type
# 2. Sequence Data Type
# 3. Set Data Type
# 4. Mapping Data Type

# Numeric Data Type: int, float, complex, boolean

# Sequence Data Type: str, list, tuple

# Set Data Type: set, frozenset

# Mapping Data Type: dict


# Numeric Data Type: int, float, complex, boolean

# integer =>  Numeric value (+ve or -ve) 

# float => Numeric value with decimal point (0.0)

# complex => Numeric value with real and imaginary part (a+bj)

# boolean => True or False